%Define a vector of numbers
numbers = [2,8,7,9,4];

%Calculate the maximum value in the vector
maxValue = max(numbers);
disp(['Maximum value: ', num2str(maxValue)]);

%Calculate the minimum value in the vector
minValue = min(numbers);
disp(['Minimum value: ', num2str(minValue)]);

%Calculate the sum of all elements in the vector
sumValue = sum(numbers);
disp(['Sum of all elements: ', num2str(sumValue)]);